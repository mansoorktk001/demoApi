package com.demoApi.demoApi;

public class Student {
	
	private String name;
	private int id;
	private String gpa;
	
	
	
	public void setGpa(String gpa) {
		this.gpa = gpa;
	}
	
	public String getGpa() {
		return gpa;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return name;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public int getId() {
		return id;
	}
	

	
	
}
